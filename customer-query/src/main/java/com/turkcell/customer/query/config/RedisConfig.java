package com.turkcell.customer.query.config;

import com.turkcell.customer.query.dto.CustomerDTO;
import com.turkcell.customer.query.dto.CustomerInvoiceDTO;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.data.redis.connection.RedisConnectionFactory;
import org.springframework.data.redis.core.RedisTemplate;
import org.springframework.data.redis.serializer.GenericJackson2JsonRedisSerializer;
import org.springframework.data.redis.serializer.StringRedisSerializer;

@Configuration
public class RedisConfig {

    @Bean
    public RedisTemplate<String, CustomerDTO> customerRedisTemplate(RedisConnectionFactory redisConnectionFactory) {
        RedisTemplate<String, CustomerDTO> template = new RedisTemplate<>();
        template.setConnectionFactory(redisConnectionFactory);
        template.setKeySerializer(new StringRedisSerializer());
        template.setValueSerializer(new GenericJackson2JsonRedisSerializer());
        return template;
    }

    @Bean
    public RedisTemplate<String, CustomerInvoiceDTO> invoiceRedisTemplate(RedisConnectionFactory redisConnectionFactory) {
        RedisTemplate<String, CustomerInvoiceDTO> template = new RedisTemplate<>();
        template.setConnectionFactory(redisConnectionFactory);
        template.setKeySerializer(new StringRedisSerializer());
        template.setValueSerializer(new GenericJackson2JsonRedisSerializer());
        return template;
    }
}
